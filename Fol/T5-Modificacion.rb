1. Modificacion

Definiciones
    ETOP = economicas tecnicas organizativas produccion (con una vale)

1.1 Movilidad Funcional
    dentro del grupo profesional [ordinaria]
        cambio de tareas dentro de tu grupo profesional
        no hace falta justificacion

    fuera del grupo profesional [extraordinaria]
        justificada con causas 
        tiempo imprescindible
        informar a los representantes de los trabajadores
        cobras el mayor salario (si es peor te quedas con el tuyo y si es mejor te quedas con ese)
        en caso mayor, puedes reclamar el ascenso si 6/12 meses || 8/24 meses


1.2 Geografica (solo cambiar domicilio)
    causas -> ETOP

    desplazamiento [temporal]
        no exceder los 12/36 meses
        te cubren:
            funciones
            salario
            gastos

    traslado [definitivo]
        antelacion minima de 30dias
        respuestas [individual/colectiva]:
            Aceptar (te pagan los gastos de mudanza)
            Rescindir(Dejar sin efecto) el contrato (indemnizacion de 20 dias por año trabajado con tope de 12 meses)
            Acotar orden + impugnacion juzgado social



